public void getMedicines(){
    al=new ArrayList<String>();
    try{
        conn=new GetConnection().getConnection();
        PreparedStatement ps=conn.prepareStatement("select name from medicines");
        ResultSet rs=ps.executeQuery();
        while(rs.next()){
            al.add(rs.getString("name"));
        }
    }
    catch(Exception e){
        System.out.print(e);
    }
}
public void getRate(JComboBox b,JTextField t){
    String medicine=(String)b.getSelectedItem();
    try{
        PreparedStatement ps=conn.prepareStatement("select price from medicines where name=?");
        ps.setString(1,medicine);
        ResultSet rs=ps.executeQuery();
        rs.next();
        rate=rs.getInt("price");
        t.setText(rate+"");
    }
    catch(Exception e){
        System.out.println(e);
    }
}
