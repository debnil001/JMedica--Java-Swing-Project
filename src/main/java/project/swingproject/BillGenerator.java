    public void updateDatabase(JComboBox name,JComboBox quan,JTextField total,JTextField rate){
        String m_name=(String)name.getSelectedItem();
        int qn=Integer.parseInt((String)quan.getSelectedItem());
        try{
        PreparedStatement ps=conn.prepareStatement("select quantity from medicines where name=?");
        ps.setString(1, m_name);
        ResultSet rs=ps.executeQuery();
        rs.next();
        int available=rs.getInt("quantity");
        int effective=available-qn;
        if(effective<0){
            JOptionPane.showMessageDialog(null, m_name+" is not available in that quantity. Available stock "+available);
            total.setText("");
            totalBill.setText("");
            quan.setSelectedIndex(0);
            flag=0;

        }
        }
        catch(Exception e){
            System.out.println(e);
        }
    }
